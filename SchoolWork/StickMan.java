public class StickMan
{
    public static void main(String[] args) throws InterruptedException {
        while(true){
            System.out.println("\r   O");
            System.out.println("\r  /|\\/  hello");
            System.out.println("\r  \\/\\");
            System.out.println("\r  / /");

            Thread.sleep(500);

            System.out.println("\r   O");
            System.out.println("\r  /|\\__ hello");
            System.out.println("\r  \\/\\");
            System.out.println("\r  / /");

            Thread.sleep(500);
        }
    }
}
